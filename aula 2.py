nome = input("Informe seu nome: ")
idade = input("Informe sua idade: ")

#primeira forma de concatenar (concatenar é o mesmo que juntar)
print("Olá ",nome," sua idade é", idade)
#segunda forma de concatenar
print("Olá {}, sua idade é {}".format(nome, idade))
#terceira forma de concatenar
print(f"Olá {nome} sua idade é {idade}")

